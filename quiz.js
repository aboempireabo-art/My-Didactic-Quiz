// الجزء الثاني: دالة توليد الواجهة (عرض الأسئلة)
// ==========================================================

function generateQuiz() {
    const quizForm = document.getElementById('quizForm');
    let htmlContent = '';
    
    quizQuestions.forEach((q, index) => {
        // ترتيب عشوائي للخيار (لتجنب حفظ مكان الإجابة الصحيحة)
        const shuffledOptions = shuffleArray([...q.options]);

        htmlContent += `<div class="question-box">`;
        htmlContent += `<p><strong>السؤال ${index + 1} من 77:</strong> ${q.question}</p>`;
        htmlContent += `<div class="options">`;
        
        shuffledOptions.forEach(option => {
            // اسم المجموعة (name) يجب أن يكون فريداً لكل سؤال (مثلاً: q0, q1, q2...)
            htmlContent += `
                <label>
                    <input type="radio" name="q${index}" value="${option}">
                    ${option}
                </label>
            `;
        });
        
        htmlContent += `</div></div>`;
    });
    
    quizForm.innerHTML = htmlContent;
}

// دالة مساعدة لترتيب العناصر بشكل عشوائي (Fisher-Yates Shuffle)
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// ==========================================================
// الجزء الثالث: دالة حساب النتيجة وعرضها
// ==========================================================

function submitQuiz() {
    let score = 0;
    let feedbackHTML = '<h3>مراجعة إجاباتك:</h3>';
    const totalQuestions = quizQuestions.length; // (يجب أن تكون 77)
    
    quizQuestions.forEach((q, index) => {
        const selectedOption = document.querySelector(`input[name="q${index}"]:checked`);
        let isCorrect = false;
        let selectedValue = 'لم يتم الإجابة';

        if (selectedOption) {
            selectedValue = selectedOption.value;
            if (selectedValue === q.answer) {
                score++;
                isCorrect = true;
            }
        }

        // بناء تقرير المراجعة لكل سؤال
        feedbackHTML += `
            <div style="border: 1px solid ${isCorrect ? '#4CAF50' : '#f44336'}; padding: 10px; margin-bottom: 10px; border-radius: 4px; background-color: ${isCorrect ? '#e6ffe6' : '#ffe6e6'};">
                <p><strong>السؤال ${index + 1}:</strong> ${q.question}</p>
                <p style="color: #333;">إجابتك: <strong>${selectedValue}</strong></p>
                <p style="color: #4CAF50;">الإجابة الصحيحة: <strong>${q.answer}</strong></p>
            </div>
        `;
    });

    const resultsDiv = document.getElementById('results');
    const scoreSpan = document.getElementById('score');
    const feedbackDiv = document.getElementById('feedback');
    
    // تحديث النتيجة وعرض لوحة النتائج
    scoreSpan.textContent = score;
    feedbackDiv.innerHTML = feedbackHTML;
    resultsDiv.style.display = 'block'; // إظهار لوحة النتائج

    // تحديث نص النتيجة لتضمين المجموع الكلي (77)
    document.getElementById('score').parentNode.innerHTML = `
        لقد حصلت على 
        <span id="score" style="font-weight: bold; color: ${score >= totalQuestions * 0.5 ? '#4CAF50' : '#f44336'};">${score}</span>
        من ${totalQuestions} إجابة صحيحة.
    `;
    
    // التمرير إلى أعلى الصفحة لرؤية النتيجة فوراً
    window.scrollTo(0, 0); 
}

